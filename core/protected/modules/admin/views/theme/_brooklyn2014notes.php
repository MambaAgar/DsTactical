<div class="alert alert-info alert-block">
	<h4>Brooklyn 2014 uses Web Store's new Integrated Cart & Checkout.</h4>
	<p>Customers cannot be required to register before completing a purchase. Instead, they have the option to create an account directly after placing an order. This is considered best practice in encouraging sales conversion.</p>
	<ul>
		<li><strong>Store Settings &gt; Customer Registration &gt; Require Account Creation</strong> - the new checkout allows customers to create an account
		after confirming their order - considered a best practice in encouraging ecommerce conversion.</li>
		<li><strong>Payments &gt; Set Display Order</strong> - currently payment options are ordered as: PayPal (if enabled), credit card payment provider and
		then any other payment options are listed below in alphabetical order.</li>
		<li><strong>Shipping &gt; Set Display Order</strong> - shipping options are listed in order from least to most expensive.</li>
		<li><strong>Shipping &gt; Global Settings &gt; Delivery Speed Format</strong> - delivery speed format is always displayed as "price label"</li>
	</ul>
	<br>
	<p>There are also new Store Address fields to fill in. Please navigate to <strong>Store Settings &gt; Store Information</strong>
	and update your Store address.</p>
